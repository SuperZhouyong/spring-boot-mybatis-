package com.resumed.sqtwin.model;

import javax.persistence.*;

@Table(name = "web_msg_map")
public class WebMsgMap {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Integer id;

    /**
     * 简历模块名称
     */
    @Column(name = "module_name")
    private String moduleName;

    /**
     * 简历模块id
     */
    @Column(name = "module_id")
    private Integer moduleId;

    /**
     * 条目名称
     */
    @Column(name = "item_name")
    private String itemName;

    /**
     * 条目id
     */
    @Column(name = "item_id")
    private Integer itemId;

    /**
     * 消息id
     */
    @Column(name = "msg_id")
    private Integer msgId;

    /**
     * 消息内容
     */
    private String msg;

    /**
     * 错误等级,1:建议; 2:错误；
     */
    private Integer level;

    /**
     * 规则
     */
    private String rule;

    /**
     * 99 没有用的
     */
    private Byte status;

    /**
     * @return id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * 获取简历模块名称
     *
     * @return module_name - 简历模块名称
     */
    public String getModuleName() {
        return moduleName;
    }

    /**
     * 设置简历模块名称
     *
     * @param moduleName 简历模块名称
     */
    public void setModuleName(String moduleName) {
        this.moduleName = moduleName;
    }

    /**
     * 获取简历模块id
     *
     * @return module_id - 简历模块id
     */
    public Integer getModuleId() {
        return moduleId;
    }

    /**
     * 设置简历模块id
     *
     * @param moduleId 简历模块id
     */
    public void setModuleId(Integer moduleId) {
        this.moduleId = moduleId;
    }

    /**
     * 获取条目名称
     *
     * @return item_name - 条目名称
     */
    public String getItemName() {
        return itemName;
    }

    /**
     * 设置条目名称
     *
     * @param itemName 条目名称
     */
    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    /**
     * 获取条目id
     *
     * @return item_id - 条目id
     */
    public Integer getItemId() {
        return itemId;
    }

    /**
     * 设置条目id
     *
     * @param itemId 条目id
     */
    public void setItemId(Integer itemId) {
        this.itemId = itemId;
    }

    /**
     * 获取消息id
     *
     * @return msg_id - 消息id
     */
    public Integer getMsgId() {
        return msgId;
    }

    /**
     * 设置消息id
     *
     * @param msgId 消息id
     */
    public void setMsgId(Integer msgId) {
        this.msgId = msgId;
    }

    /**
     * 获取消息内容
     *
     * @return msg - 消息内容
     */
    public String getMsg() {
        return msg;
    }

    /**
     * 设置消息内容
     *
     * @param msg 消息内容
     */
    public void setMsg(String msg) {
        this.msg = msg;
    }

    /**
     * 获取错误等级,1:建议; 2:错误；
     *
     * @return level - 错误等级,1:建议; 2:错误；
     */
    public Integer getLevel() {
        return level;
    }

    /**
     * 设置错误等级,1:建议; 2:错误；
     *
     * @param level 错误等级,1:建议; 2:错误；
     */
    public void setLevel(Integer level) {
        this.level = level;
    }

    /**
     * 获取规则
     *
     * @return rule - 规则
     */
    public String getRule() {
        return rule;
    }

    /**
     * 设置规则
     *
     * @param rule 规则
     */
    public void setRule(String rule) {
        this.rule = rule;
    }

    /**
     * 获取99 没有用的
     *
     * @return status - 99 没有用的
     */
    public Byte getStatus() {
        return status;
    }

    /**
     * 设置99 没有用的
     *
     * @param status 99 没有用的
     */
    public void setStatus(Byte status) {
        this.status = status;
    }
}