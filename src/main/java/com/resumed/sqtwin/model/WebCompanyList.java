package com.resumed.sqtwin.model;

import javax.persistence.*;

@Table(name = "web_company_list")
public class WebCompanyList {
    @Id
    @Column(name = "companyId")
    private Integer companyid;

    @Column(name = "cn_name")
    private String cnName;

    /**
     * 英文简称
     */
    @Column(name = "en_name")
    private String enName;

    /**
     * 英文全称
     */
    @Column(name = "full_en_name")
    private String fullEnName;

    /**
     * 中文全称
     */
    @Column(name = "full_cn_name")
    private String fullCnName;

    @Column(name = "is_world_top")
    private Byte isWorldTop;

    @Column(name = "is_china_top")
    private Byte isChinaTop;

    /**
     * 是否是中国上市500强
     */
    @Column(name = "is_on_china_top")
    private Byte isOnChinaTop;

    private Byte status;

    /**
     * @return companyId
     */
    public Integer getCompanyid() {
        return companyid;
    }

    /**
     * @param companyid
     */
    public void setCompanyid(Integer companyid) {
        this.companyid = companyid;
    }

    /**
     * @return cn_name
     */
    public String getCnName() {
        return cnName;
    }

    /**
     * @param cnName
     */
    public void setCnName(String cnName) {
        this.cnName = cnName;
    }

    /**
     * 获取英文简称
     *
     * @return en_name - 英文简称
     */
    public String getEnName() {
        return enName;
    }

    /**
     * 设置英文简称
     *
     * @param enName 英文简称
     */
    public void setEnName(String enName) {
        this.enName = enName;
    }

    /**
     * 获取英文全称
     *
     * @return full_en_name - 英文全称
     */
    public String getFullEnName() {
        return fullEnName;
    }

    /**
     * 设置英文全称
     *
     * @param fullEnName 英文全称
     */
    public void setFullEnName(String fullEnName) {
        this.fullEnName = fullEnName;
    }

    /**
     * 获取中文全称
     *
     * @return full_cn_name - 中文全称
     */
    public String getFullCnName() {
        return fullCnName;
    }

    /**
     * 设置中文全称
     *
     * @param fullCnName 中文全称
     */
    public void setFullCnName(String fullCnName) {
        this.fullCnName = fullCnName;
    }

    /**
     * @return is_world_top
     */
    public Byte getIsWorldTop() {
        return isWorldTop;
    }

    /**
     * @param isWorldTop
     */
    public void setIsWorldTop(Byte isWorldTop) {
        this.isWorldTop = isWorldTop;
    }

    /**
     * @return is_china_top
     */
    public Byte getIsChinaTop() {
        return isChinaTop;
    }

    /**
     * @param isChinaTop
     */
    public void setIsChinaTop(Byte isChinaTop) {
        this.isChinaTop = isChinaTop;
    }

    /**
     * 获取是否是中国上市500强
     *
     * @return is_on_china_top - 是否是中国上市500强
     */
    public Byte getIsOnChinaTop() {
        return isOnChinaTop;
    }

    /**
     * 设置是否是中国上市500强
     *
     * @param isOnChinaTop 是否是中国上市500强
     */
    public void setIsOnChinaTop(Byte isOnChinaTop) {
        this.isOnChinaTop = isOnChinaTop;
    }

    /**
     * @return status
     */
    public Byte getStatus() {
        return status;
    }

    /**
     * @param status
     */
    public void setStatus(Byte status) {
        this.status = status;
    }
}